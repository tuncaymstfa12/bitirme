let currentLang = loadLang();

function loadLang() {
  try {
    return localStorage.getItem(LANG_STORAGE_KEY) || 'tr';
  } catch {
    return 'tr';
  }
}

/**
 * Get translated string by key.
 * Supports {placeholder} interpolation: t('auth.welcomeMsg', { name: 'Ali' })
 */
export function t(key, params = {}) {
  const dict = translations[currentLang] || translations.tr;
  let value = dict[key];
  if (value === undefined) {
    // Fallback to Turkish
    value = translations.tr[key];
  }
  if (value === undefined) return key;

  // Interpolate {placeholders}
  if (typeof value === 'string' && params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.replaceAll(`{${k}}`, v);
    }
  }
  return value;
}

/**
 * Get current language code
 */
export function getLang() {
  return currentLang;
}

/**
 * Set language and persist
 */
export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  try {
    localStorage.setItem(LANG_STORAGE_KEY, lang);
  } catch { /* ignore */ }
}

/**
 * Get available languages
 */
export function getAvailableLanguages() {
  return [
    { code: 'tr', label: 'Türkçe' },
    { code: 'en', label: 'English' },
  ];
}

/**
 * Format date using current locale
 */
export function formatDateLocale(dateString) {
  const locale = currentLang === 'tr' ? 'tr-TR' : 'en-US';
  const date = new Date(dateString);
  return date.toLocaleDateString(locale, { month: 'short', day: 'numeric' });
}

/**
 * Format date with full detail using current locale
 */
export function formatDateFullLocale(dateString) {
  const locale = currentLang === 'tr' ? 'tr-TR' : 'en-US';
  const date = new Date(dateString);
  return date.toLocaleDateString(locale, { weekday: 'short', month: 'short', day: 'numeric', year: 'numeric' });
}
